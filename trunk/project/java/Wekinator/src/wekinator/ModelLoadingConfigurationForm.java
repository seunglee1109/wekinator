    public ModelLoadingConfigurationForm(String[] currNames, String[] newNames, LearningAlgorithmConfigurationPanel c) {
        caller = c;
        this.newData = newData;
        this.l = l;
        this.laFile = laFile;
        mCurrNames = new String[currNames.length];
        mNewNames = new String[newNames.length];
        combos = new JComboBox[newNames.length];
        System.arraycopy(currNames, 0, mCurrNames, 0, currNames.length);
        System.arraycopy(newNames, 0, mNewNames, 0, newNames.length);
        initComponents();
        for (int i = 0; i < newNames.length; i++) {
            JPanel p = new JPanel();
            p.setPreferredSize(new Dimension(345, 30));
            p.add(new JLabel(newNames[i]));
            JComboBox cb = new JComboBox(currNames);
            if (i < currNames.length) {
                cb.setSelectedIndex(i);
            }
            combos[i] = cb;
            p.add(cb);
            panelFeatureList.add(p);
        }
        this.repaint();
    }

