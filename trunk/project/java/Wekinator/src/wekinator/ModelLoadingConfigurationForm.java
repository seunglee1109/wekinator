    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Check counts
        int[] counts = new int[mCurrNames.length];
        for (int i = 0; i < mNewNames.length; i++) {
            counts[combos[i].getSelectedIndex()]++;
        }
        boolean err= false;
        String errString = "Error: One or more current features is selected twice:\n";
        for (int i = 0; i < mCurrNames.length; i++) {
            if (counts[i] > 1) {
                err = true;
                errString += mCurrNames[i] + " ";
            }
        }
        if (err) {
          JOptionPane.showMessageDialog(this, errString, "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println(errString);
        }
        else {
            commit();
            this.dispose();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

